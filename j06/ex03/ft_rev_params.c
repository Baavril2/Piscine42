void	ft_putchar(char c);

int		main(int ac, char **av)
{
	int i;
	int j;
	int c;

	i = ac - 1;
	c = 1;
	while (i > 0)
	{
		j = 0;
		while (av[i][j] != '\0')
		{
			ft_putchar(av[i][j]);
			j++;
		}
		ft_putchar('\n');
		c++;
		i--;
	}
}
