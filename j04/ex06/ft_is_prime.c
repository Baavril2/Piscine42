int		ft_is_prime(int nb)
{
	int	i;

	i = 0;
	if (nb == 0 || nb == 1)
		return (0);
	if (nb < 0)
		return (0);
	i = 1;
	while (i < nb)
	{
		i++;
		if (nb % 2 == 0 || nb % 3 == 0)
			return (0);
	}
	return (1);
}
